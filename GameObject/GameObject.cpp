//*****************************************************************************
//
// 更新
//
//*****************************************************************************
void GameObject::update()
{
	for (auto it : this->mComponentsMap)
	{
		if (it.second->mStart == true)
		{
			it.second->update();
		}
		else
		{
			it.second->start();
			it.second->mStart = true;
		}
	}
}