//*****************************************************************************
GameObject::~GameObject(void)
{
	this->mComponents.clear();
}

//*****************************************************************************
//
// 初期化
//
//*****************************************************************************
void GameObject::start(void)
{

}

//*****************************************************************************
//
// 更新
//
//*****************************************************************************
void GameObject::update(void)
{
	for (auto it : this->mComponents)
	{
		if (it.second->mIsStart == true)
		{
			it.second->update();
		}
		else
		{
			it.second->start();
			it.second->mIsStart = true;
		}
	}
}