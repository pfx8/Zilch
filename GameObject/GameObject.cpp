//*****************************************************************************
//
// 更新
//
//*****************************************************************************
void GameObject::update()
{
	for (auto it : mComponents)
	{
		if (it->isStart == true)
		{
			it->update();
		}
		else
		{
			it->start();
			it->isStart = true;
		}
	}
}