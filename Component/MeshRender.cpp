void MeshRender::draw()
{
	// モデルのワールド変換行列を取得
	Transform* trans = this->gameObject->getComponent<Transform>("trans");
	// カメラ情報を取得
	Camera* camera = this->gameObject->mScene->getGameObject("mainCamera")->getComponent<Camera>("camera");

	model->draw(trans, camera);
}
