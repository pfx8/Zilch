//*****************************************************************************
//
// ImGuiでPointLightのデータを出す
//
//*****************************************************************************
void PointLight::drawImGui()
{
	ImGui::Text(u8"ポイントライト");
	float* v[3] = { &this->mLightPos.x, &this->mLightPos.y, &this->mLightPos.z };
	ImGui::InputFloat3("pos", *v);
}