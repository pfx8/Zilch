//*****************************************************************************
//
// 時間を更新
//
//*****************************************************************************
void GameTimes::update()
{
	// 今の時間を取得
	mCurrentTime = timeGetTime();

	// 1回更新の時間差を計算(second)
	mDeltaTime = (mCurrentTime - mLastTime) / 1000.0;

	// 前の時間を更新
	mLastTime = mCurrentTime;
}