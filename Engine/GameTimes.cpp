//*****************************************************************************
//
// 更新
//
//*****************************************************************************
void GameTimes::update(void)
{
	this->mCurrentTime = timeGetTime();
	this->mDeltaTime = (this->mCurrentTime - this->mLastUpdateTime) / 1000.0f;
	this->mLastUpdateTime = this->mCurrentTime;
}