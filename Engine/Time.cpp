//*****************************************************************************
//
// 時間を更新
//
//*****************************************************************************
void Time::update()
{
	// 今の時間を取得
	mCurrentTime = timeGetTime();

	// 1回更新の時間差を計算
	mDeltaTime = mCurrentTime - mLastTime;

	// 前の時間を更新
	mLastTime = mCurrentTime;
}